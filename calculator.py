# Python program for simple calculator
x = int(input("Enter firstnumber"))
y = int(input("Enter secondnumber"))
operator = input("Enter operator")

if operator == "+":
    print("Result is :", x + y)
elif operator == "-":
    print("Result is ", x - y)
elif operator == "*":
    print("Result is ", x * y)
elif operator == "/":
    print("Result is ", x / y)
else:
    print("Invalid operator")

